const KEY = 'xala_lang';
const DEFAULT = 'en';
let current = DEFAULT;

try {
  const saved = localStorage.getItem(KEY);
  if (saved && STRINGS[saved]) current = saved;
} catch (e) { /* sense localStorage: continuem amb el per defecte */ }

export function getLang() { return current; }

export function setLang(code) {
  if (!STRINGS[code]) return;
  current = code;
  try { localStorage.setItem(KEY, code); } catch (e) { /* ignora */ }
}

// Substitueix {clau} per vars[clau] dins d'una cadena.
function fill(str, vars) {
  if (!vars) return str;
  return str.replace(/\{(\w+)\}/g, (m, k) => (k in vars ? vars[k] : m));
}

// Tradueix una clau. Si no existeix en l'idioma actual, cau a l'anglès i,
// si tampoc hi és, retorna la mateixa clau (per detectar-ho fàcilment).
export function t(key, vars) {
  const table = STRINGS[current] || STRINGS[DEFAULT];
  let s = (table && key in table) ? table[key]
        : (key in STRINGS[DEFAULT] ? STRINGS[DEFAULT][key] : key);
  return fill(s, vars);
}

// Plural simple: tria `base + '_one'` o `base + '_other'` segons n,
// i hi injecta {n} automàticament (a més de vars addicionals).
export function tc(n, base, vars) {
  const key = base + (n === 1 ? '_one' : '_other');
  return t(key, Object.assign({ n }, vars || {}));
}

// Uneix una llista de noms amb la conjunció localitzada ("i", "and"...).
export function joinAnd(names) {
  if (!names || names.length === 0) return '';
  if (names.length === 1) return names[0];
  return names.join(' ' + t('common.and') + ' ');
}
